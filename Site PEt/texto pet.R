Mercado de trabalho
O estatístico pode trabalhar em agências governamentais, institutos de pesquisa, empresas do setor financeiro e de tecnologia, grandes corporações e escritórios de consultoria, na indústria, criação de banco de dados, bioestatística, análise de perfil de consumidor, até mesmo na área jurídica e qualquer outro setor que envolva a coleta e análise de dados.
Atualmente, há uma grande demanda por estatísticos no mercado de trabalho. Portanto, é uma profissão bem remunerada. A expansão desta área se deve principalmente às grandes bases de dados, conhecidas como big data.

Áreas de atuação do estatístico
De maneira geral, o estatístico formado na UFPR é um profissional que possui sólidos conhecimentos em estatística, matemática e computação. Será capaz de utilizar as ferramentas necessárias para coletar, organizar, analisar e interpretar diversos tipos de dados. 
Sendo  capaz de resolver problemas usuais de diversas áreas de atuação (??atuações??), como saúde, agronegócio, ciências sociais, linguística, telecomunicações, informática, setor industrial, setor financeiro e inteligência de mercado, marketing, entre tantas outras áreas onde for necessário realizar análise de informações.
