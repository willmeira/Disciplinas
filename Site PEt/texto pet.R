Informações do curso
Fundado em 1974, o curso de Bacharelado em  Estatística da Universidade Federal do Paraná, oferta anualmente 66 vagas, com aulas no período noturno e com duração média de nove semestres.
A maioria das atividades do curso é desenvolvida no Laboratório de Estatística, onde o aluno tem à sua disposição salas de aulas com microcomputadores que são utilizados para o aprendizado de técnicas estatísticas. 
Devido à diversidade da área de atuação do profissional de Estatística, o curso tem  como objetivo formar profissionais capacitados para atender às necessidades da sociedade, tendo em vista as exigências e a crescente demanda do mercado.
Vivemos na era do big data. Temos muitos dados, mas ainda não sabemos maneiras eficientes de processá-los. Um dos maiores problemas atualmente é encontrar informações úteis neste universo de informações presentes no mundo. E ninguém melhor do que um estatístico para fazer isto.

Estrutura curricular foi dividida em 5 módulos que totalizam 3.000 horas:
    1 - Formação fundamental (1.590 horas): Disciplinas obrigatórias que fornecem base de matemática, computação, probabilidade, estatística e estatística computacional.
2 - Formação específica (540 horas): Disciplinas obrigatórias que aplicam os conhecimentos de base, principalmente nas áreas de bioestatística, controle de processos industriais e ciências atuariais. 
3 - Formação complementar obrigatória (270 horas): Composta pelo Trabalho de Conclusão de Curso (TCC).
4 - Formação complementar optativa (300 horas): Disciplinas optativas que permitem ao aluno aprofundar-se em subáreas de acordo com seus interesses e aptidões.
5 - Atividades formativas (300 horas):  Disciplinas eletivas, estágio não-obrigatório, monitoria, iniciação científica, extensão, educação à distância (EAD), representação acadêmica, Programa Especial de Treinamento (PET), Empresa Júnior, entre outros.
